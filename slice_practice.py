# slice_practice.py
# スライス（部分取り出し）の練習コード

# --- リストでのスライス ---
nums = [0, 1, 2, 3, 4, 5, 6]

print(nums[2:5])     # 2番目から4番目まで [2, 3, 4]
print(nums[:4])      # 先頭から3番目まで [0, 1, 2, 3]
print(nums[3:])      # 3番目から最後まで [3, 4, 5, 6]
print(nums[:])       # 全部コピー [0, 1, 2, 3, 4, 5, 6]
print(nums[-3:])     # 末尾から3つ [4, 5, 6]
print(nums[::2])     # ステップ指定（2ごとに） [0, 2, 4, 6]
print(nums[::-1])    # 逆順 [6, 5, 4, 3, 2, 1, 0]

# --- 文字列でのスライス ---
word = "Python"

print(word[0:2])     # 先頭2文字 "Py"
print(word[2:])      # 2文字目以降 "thon"
print(word[-3:])     # 最後の3文字 "hon"
print(word[::-1])    # 逆順 "nohtyP"


# スライスの練習問題集

nums = [0, 1, 2, 3, 4, 5, 6]
word = "Python"

# --- 問題 1 ---
# nums から [2, 3, 4] を取り出してください。
q1 = nums[2:5]
print(q1)

# --- 問題 2 ---
# nums の先頭3つ [0, 1, 2] を取り出してください。
q2 = nums[:3]
print(q2)

# --- 問題 3 ---
# nums の最後の2つ [5, 6] を取り出してください。
q3 = nums[-2:]
print(q3)

# --- 問題 4 ---
# nums の偶数だけ [0, 2, 4, 6] を取り出してください。
q4 = nums[::2]
print(q4)

# --- 問題 5 ---
# nums を逆順にしてください [6, 5, 4, 3, 2, 1, 0]
q5 = nums[::-1]
print(q5)

# --- 問題 6 ---
# word から "Py" を取り出してください。
q6 = word[:2]
print(q6)

# --- 問題 7 ---
# word から "thon" を取り出してください。
q7 = word[2:]
print(q7)

# --- 問題 8 ---
# word を逆順にしてください "nohtyP"
q8 = word[::-1]
print(q8)

# --- 問題 9 ---
# nums の最後の3つ [4, 5, 6] を取り出してください。
q9 = nums[-3:]
print(q9)

# --- 問題 10 ---
# nums の最後の要素 6 を取り出してください。
q10 = nums[-1]
print(q10)

# --- 問題 11 ---
# nums の末尾から2番目と3番目 [4, 5] を取り出してください。
q11 = nums[-3:-1]
print(q11)

# --- 問題 12 ---
# word から "on" を取り出してください（末尾2文字）。
q12 = word[-2:]
print(q12)

# --- 問題 13 ---
# word の "th" を取り出してください（負のインデックスを使う）。
q13 = word[-4:-2]
print(q13)
